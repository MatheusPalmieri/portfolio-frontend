export async function getProjects(): Promise<{ data: IProject[]; status: number }> {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve({
        data: projects,
        status: 200,
      });
    }, 2000);
  });
}

export async function getProjectBySlug(slug: string): Promise<{ data: IProject; status: number }> {
  const project: IProject | undefined = projects.find((project: IProject) => project.slug === slug);

  if (!project) throw { message: 'Projeto nÃ£o encontrado.', status: 400 };

  return new Promise((resolve) => {
    setTimeout(() => {
      resolve({
        data: project,
        status: 200,
      });
    }, 50);
  });
}
